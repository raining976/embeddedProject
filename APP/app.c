void AppInit(void)
{
	uint32_t Retry = 0;
	SysTick_Init();
	BSP_LedInit();
	fml_beep_init();
	bsp_usart_init();
	
	BluetoothInit();
	BluetoothConfig();
	fml_motor_init();
	fml_ir_init();
	OLED_Init();
	//OLED_ShowChar(1, 1, 'C');
	OLED_ShowChineseWord(2,3,"欢迎使用");
	SysInit();

	
	while( DHT11Init() )
	{
		printf("正在初始化.....请耐心等待\r\n!");
		Retry++;
		if(Retry >= 0xfffffffe)
		{
			printf("DHT11初始化失败....请检查！\r\n!");
			return ;
		}
	}
	printf("DHT11初始化成功！\r\n");
	printf("正在等待DHT11越过不稳定期，请稍后....\r\n");
	fml_delay_ms(2000);	
	printf("DHT11稳定\r\n");
	
}

//page由遥控器任务或蓝牙任务控制
void oledDispalyTask(void)
{	
	static uint8_t sys_on_page = 0;
	char buff[64] = {0};
	
	switch(sys_parm.page)
	{
		case 0:
			
			switch(sys_on_page)
			{
				case 0:
					OLED_ShowFrame(tupian1);
					sys_on_page = 1;
					break;
				case 1:
					OLED_ShowFrame(tupian2);
					sys_on_page = 2;
				break;
				case 2:
					OLED_ShowFrame(tupian3);
					sys_on_page = 3;
					break;
				case 3:
					OLED_ShowFrame(tupian4);
					sys_on_page = 4;
					break;
				case 4:
					OLED_ShowFrame(tupian5);
					sys_on_page = 0;
					sys_parm.page = 1;
					sys_parm.dataChange = 1;
				break;
			}
		case 1:			//风扇状态显示页
			if (sys_parm.dataChange == 0)
				break;
			sys_parm.dataChange = 0;
			if(sys_parm.flag_state_change == 1 || sys_parm.flag_first == 0) OLED_Clear();
			if (sys_parm.motor_state == 0)
				OLED_ShowString(1,1,"Fan Statue:OFF");
			else
			{
				OLED_ShowString(1,1,"Fan Statue:On");
				sprintf(buff,"Fan Gear:%d",sys_parm.motor_speed);
				OLED_ShowString(2,1,buff);
				printf("%d\r\n",sys_parm.motor_state);
			}
			sys_parm.flag_state_change = 0;
			sys_parm.flag_first = 1;
		break;
		case 2:			//蓝牙连接状态+温湿度数据
			if (sys_parm.dataChange == 0)
				break;
			sys_parm.dataChange = 0;
			OLED_Clear();
			if (sys_parm.bt_state == 1)
				OLED_ShowString(1,1,"Bt connected");
			else
				OLED_ShowString(1,1,"Bt not connected");
			sprintf(buff,"temperature :%d",sys_parm.temprature);
			OLED_ShowString(2,1,buff);
			sprintf(buff,"humidity :%d%%",sys_parm.humidity);
			OLED_ShowString(3,1,buff);
			break;
		default:
			break;
	}
}


